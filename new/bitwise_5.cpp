#include<iostream>
using namespace std;

int main(){
    //BITWISE OPERATOR

    int a = 4;
    int b = 6;

    cout<<"a&b "<<(a&b)<<endl;  // Logical And operator
    cout<<"a|b "<<(a|b)<<endl;  // Logical Or operator
    cout<<"~a "<<~a<<endl;  // Logical Not operator
    cout<<"a^b "<<(a^b)<<endl;  // Logical XOR operator
}